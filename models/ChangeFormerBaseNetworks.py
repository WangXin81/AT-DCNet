def init_linear(linear):
    init.xavier_normal(linear.weight)
    linear.bias.data.zero_()


def init_conv(conv, glu=True):
    init.kaiming_normal(conv.weight)
    if conv.bias is not None:
        conv.bias.data.zero_()

# 用于生成在初始化时对权重施加均等化的神经网络
class EqualLR:
    def __init__(self, name):
        self.name = name

    def compute_weight(self, module):  # 从该模块中获取原始的权重参数，然后根据均等化的公式计算新的权重
        weight = getattr(module, self.name + '_orig')
        fan_in = weight.data.size(1) * weight.data[0][0].numel()

        return weight * sqrt(2 / fan_in)

    @staticmethod
    def apply(module, name):  # 用于将均等化操作应用到神经网络模块中的权重
        fn = EqualLR(name)        # 创建一个 EqualLR 类的实例

        weight = getattr(module, name)    # 获取模块中的原始权重参数
        del module._parameters[name]     # 删除模块中的原始权重参数。
        module.register_parameter(name + ' _orig', nn.Parameter(weight.data))
        module.register_forward_pre_hook(fn)   # 注册前向传播钩子，以在前向传播过程中应用均等化操作。

        return fn

    def __call__(self, module, input):
        weight = self.compute_weight(module)    # 计算均等化后的权重
        setattr(module, self.name, weight)   # 将计算后的均等化权重 weight 设置为神经网络模块 module 中的指定权重参数 self.name


def equal_lr(module, name='weight'):
    EqualLR.apply(module, name)

